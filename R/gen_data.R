#' \item{`byear`}{Birth year}
#' \item{`cage`}{Age at first birth}
#' \item{`rel_time`}{Relative time to first birth}
#' \item{`y`}{Outcome variable}
#' }
#' @source Generated by `gen_data()` with seed `1234`
